/* Offsets in the template */
#define PLEN_OFF       3  /* param length (2 bytes, big-endian) */
#define OUTER_SEQ_OFF  7  /* outer sequence length (2 bytes at offset 7-8) */
#define DESC_TEXT_LEN_OFF  159  /* text length byte for descriptor (1 byte) */
#define DESC_DATA_OFF  161 /* start of keyboard descriptor data */
#define DESC_DATA_LEN  99  /* length of keyboard descriptor */
#define OUTER_SEQ_LEN_OFF  153  /* outer seq length byte for 0x0206 */
#define INNER_SEQ_LEN_OFF  155  /* inner seq length byte for 0x0206 */
#define AFTER_DESC_OFF 260 /* offset after keyboard descriptor */

int main(void) {
    setbuf(stdout, NULL);

    int desc_diff = (int)sizeof(our_desc) - DESC_DATA_LEN;  /* size difference */
    int new_total = sizeof(keyb_template) + desc_diff;

    printf("Keyboard desc: %d bytes, Our desc: %zu bytes, diff: %d\n",
           DESC_DATA_LEN, sizeof(our_desc), desc_diff);
    printf("Template: %zu bytes, New: %d bytes\n", sizeof(keyb_template), new_total);

    uint8_t *msg = malloc(new_total);

    /* Copy header up to descriptor data */
    memcpy(msg, keyb_template, DESC_DATA_OFF);

    /* Replace descriptor */
    memcpy(msg + DESC_DATA_OFF, our_desc, sizeof(our_desc));

    /* Copy remaining attributes after descriptor */
    memcpy(msg + DESC_DATA_OFF + sizeof(our_desc),
           keyb_template + AFTER_DESC_OFF,
           sizeof(keyb_template) - AFTER_DESC_OFF);

    /* Fix lengths */
    /* param length (bytes 3-4): total - 5 (header) */
    int plen = new_total - 5;
    msg[PLEN_OFF] = (plen >> 8) & 0xFF;
    msg[PLEN_OFF + 1] = plen & 0xFF;

    /* Outer SDP sequence length (bytes 7-8) */
    int outer_seq_len = plen - 2; /* minus the sequence header type+len bytes... actually -3 */
    /* The outer seq at offset 6: 36 XX XX — 2-byte length */
    outer_seq_len = new_total - 5 - 3; /* total - header - seq_header(3) */
    msg[7] = (outer_seq_len >> 8) & 0xFF;
    msg[8] = outer_seq_len & 0xFF;

    /* descriptor text length: 25 XX (1-byte) or 26 XX XX (2-byte) */
    if (sizeof(our_desc) < 256) {
        msg[DESC_TEXT_LEN_OFF] = sizeof(our_desc);
    }

    /* inner seq length: sequence at offset 155 */
    /* inner = uint8(0x22=1byte+1type=2) + text(1type+1len+data) = 2 + 2 + sizeof(our_desc) */
    int inner_len = 2 + 2 + sizeof(our_desc);
    msg[INNER_SEQ_LEN_OFF] = inner_len;

    /* outer seq length at offset 153: inner_seq_header(2) + inner_len */
    int outer_0206_len = 2 + inner_len;
    msg[OUTER_SEQ_LEN_OFF] = outer_0206_len;

    /* Also change service name from "HID Keyboard" to "TabletPen" */
    /* "HID Keyboard" is at offset 93: 25 0c 48 49 44 20 4b 65 79 62 6f 61 72 64 */
    /* "TabletPen" is 9 bytes, so: 25 09 54 61 62 6c 65 74 50 65 6e */
    /* But changing name length would shift all offsets... skip for now */

    printf("Sending %d bytes to SDP server...\n", new_total);

    /* Connect to SDP server */
    int fd = socket(AF_UNIX, SOCK_STREAM, 0);
    if (fd < 0) { perror("socket"); return 1; }

    struct sockaddr_un addr = {0};
    addr.sun_family = AF_UNIX;
    strncpy(addr.sun_path, SDP_UNIX_PATH, sizeof(addr.sun_path) - 1);

    if (connect(fd, (struct sockaddr *)&addr, sizeof(addr)) < 0) {
        perror("connect SDP");
        return 1;
    }

    write(fd, msg, new_total);

    uint8_t rsp[64];
    int n = read(fd, rsp, sizeof(rsp));
    if (n >= 5 && rsp[0] == 0x76) {
        uint32_t handle = (rsp[5] << 24) | (rsp[6] << 16) | (rsp[7] << 8) | rsp[8];
        printf("SDP record registered! handle=0x%08x\n", handle);
    } else {
        printf("Response: %d bytes, opcode=0x%02x\n", n, n > 0 ? rsp[0] : 0);
    }

    /* Keep alive — record removed when we disconnect */
    printf("Record active. Press Ctrl+C to remove.\n");
    while (1) sleep(3600);

    free(msg);
    close(fd);
    return 0;
}
